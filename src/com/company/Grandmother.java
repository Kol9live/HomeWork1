package com.company;

public class Grandmother {
    private int age;
    private String name;

    public Grandmother(int age) {
    }

    public int getAge() {
        return age;
    }

    public String getName() {
        return name;
    }

    public Grandmother(int age, String name) {
        this.age = age;
        this.name = name;
    }
}
